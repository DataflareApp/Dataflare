#[derive(Debug)]
pub enum ColumnType {
    BuiltIn(Type),
    Custom(String),
}

impl From<ColumnType> for String {
    fn from(value: ColumnType) -> Self {
        match value {
            ColumnType::BuiltIn(t) => t.to_string(),
            ColumnType::Custom(t) => t,
        }
    }
}

impl ColumnType {
    pub fn from_oid(oid: u32) -> Option<Self> {
        Type::from_repr(oid).map(ColumnType::BuiltIn)
    }
}
